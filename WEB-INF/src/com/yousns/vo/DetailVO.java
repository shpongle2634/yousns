package com.yousns.vo;

public class DetailVO {

	private String MyIntroduce;
	private String Address;
	private String Phone;
	private String Job;
	private String School;
	private short married;
	public String getMyIntroduce() {
		return MyIntroduce;
	}
	public void setMyIntroduce(String myIntroduce) {
		MyIntroduce = myIntroduce;
	}
	public String getAddress() {
		return Address;
	}
	public void setAddress(String address) {
		Address = address;
	}
	public String getPhone() {
		return Phone;
	}
	public void setPhone(String phone) {
		Phone = phone;
	}
	public String getJob() {
		return Job;
	}
	public void setJob(String job) {
		Job = job;
	}
	public String getSchool() {
		return School;
	}
	public void setSchool(String school) {
		School = school;
	}
	public short isMarried() {
		return married;
	}
	public void setMarried(short married) {
		this.married = married;
	}
	
}
